public class chepPhat {
    public static void main(String[] args) {
        for (int i = 0; i <= 10 ; i++) {
            System.out.println(i + ". Xin chao");
        }
        System.out.println("\n");
        for (int i = 10; i > 0 ; i--) {
            System.out.println(i + ". Tam biet");
        }
    }
}
